#rfind() search backwards in a string

str1 = "Python number string examples"

str2 = "num"

print(str1.find(str2)) #looking for index

print(str1.find(str2, 10))  # start index 10

print(str1.find(str2, 15, 20)) #start and eding index 15 and 20


print(str1.rfind(str2)) #looking for index

print(str1.rfind(str2, 10))  # start index 10

print(str1.rfind(str2, 15, 20)) #start and eding index 15 and 20




